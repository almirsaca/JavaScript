// == comparação implicita

const numero = 5;
const texto = "5";

console.log('numero == texto -> ' + (numero == texto));

// === comparação Explicita

console.log('numero === texto -> ' + (numero === texto));

// Tipo de dados das variaveis -> typeof
console.clear();

console.log('typeof(numero) -> ' + typeof numero);
console.log('typeof(texto) -> ' + typeof texto);

// Conversão Explicita

Number();
String();